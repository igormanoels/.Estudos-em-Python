'''
    +       --> Soma
    -       --> Subtração
    *       --> Multiplicação
    /       --> Divisão
    **      --> Potência
    //      --> Apenas a parte inteira da divisão
    %       --> Móludo ou resto
'''


print("Potencia: ", 2 ** 4)


print("divisão: ", 56 / 3)

print("Inteiro da divisão: ", 56 // 3)

print("Resto da divisão: ", 56 % 3)

