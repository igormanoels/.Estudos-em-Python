# 3. Receba a base e a altura de um triângulo. Calcule e mostre a sua área.

print("Algoritmo: Cálcula a área de um triângulo.")

altura = float(input("Favor informar o valor da altura: "))
base = float(input("Favor informar o valor da base: "))

area = ((base * altura) / 2)

print("Área do Triângulo = ", area)
