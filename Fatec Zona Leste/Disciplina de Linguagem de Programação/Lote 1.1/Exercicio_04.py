# 4. Receba a temperatura em graus Celsius.
# Calcule e mostre a sua temperatura convertida em fahrenheit F = (9*C+160) /5.

print("Algoritmo: Converte a temperatura atual para Fahrenheit.")

tempC = int(input("Favor informar a temperatura atual: "))
fahr = ((9 * tempC + 160) / 5)

print("A temperatura em Fahrenheit: ", fahr)
